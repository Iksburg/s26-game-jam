namespace CatWorld.Cats
{
    /// <summary>
    /// Черта характера кота (7 возможных). Черта может быть врождённой или приобретённой.
    /// Модификаторы стоимости (из концепта) реализуются в задаче экономики, не здесь.
    /// </summary>
    public enum CatTrait
    {
        Sleepy,    // Соня — чаще спит
        Quiet,     // Тихоня — почти не мяукает (+5% стоимости)
        Clean,     // Чистюля — пачкается реже (+5% стоимости)
        Champion,  // Чемпион — победа в выставке (+20% стоимости)
        Grumpy,    // Злыдень — шипит, когда трогают (-5% стоимости)
        Meower,    // Мяукалка — постоянно мяукает (-5% стоимости)
        Messy      // Грязнуля — пачкается чаще (-10% стоимости)
    }
}
